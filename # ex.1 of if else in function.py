def tensname(num):
    if num >= 90 : return "ninety"
    if num >= 80 : return "eighty"
    if num >= 70 : return "seventy"
    if num >= 60 : return "sixty"
    if num >= 50 : return "fifty"
    if num >= 40 : return "forty"
    if num >= 30 : return "thirty"
    if num >= 20 : return "twenty"
    return " "


